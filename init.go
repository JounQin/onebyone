package onebyone

import (
	"encoding/json"
	"strings"

	"github.com/cdle/sillyGirl/core"
	"github.com/gin-gonic/gin"
)

type AutoGenerated struct {
	PtPin   string `json:"pt_pin"`
	Message string `json:"message"`
}

func init() {

	core.Server.POST("/onebyone/push", func(c *gin.Context) {
		data, _ := c.GetRawData()
		ag := &AutoGenerated{}
		json.Unmarshal(data, ag)
		ptPin := ag.PtPin
		message := ag.Message
		for _, tp := range []string{
			"qq", "tg", "wx",
		} {
			core.Bucket("pin" + strings.ToUpper(tp)).Foreach(func(k, v []byte) error {
				tempMsg := message
				if tp == "wx" {
					tempMsg = translate(message)
				}
				if string(k) == ptPin && ptPin != "" {
					if push, ok := core.Pushs[tp]; ok {
						push(string(v), tempMsg)
					}
				}
				return nil
			})
		}
		c.String(200, "ok")
	})
}

func translate(str string) string {
	tempMsg := str
	tempMsg = strings.Replace(tempMsg, "‚≠ï", "[emoji=\\u2b55]" -1)
	tempMsg = strings.Replace(tempMsg, "üßß", "[emoji=\\uD83E\\uDDE7]" -1)
	tempMsg = strings.Replace(tempMsg, "ü•ö", "[emoji=\\ud83e\\udd5a]" -1)
	tempMsg = strings.Replace(tempMsg, "üí∞", "[emoji=\\ud83d\\udcb0]" -1)
	tempMsg = strings.Replace(tempMsg, "‚è∞", "[emoji=\\u23f0]" -1)
	tempMsg = strings.Replace(tempMsg, "üçí", "[emoji=\\ud83c\\udf52\\u00a]" -1)
	tempMsg = strings.Replace(tempMsg, "üê∂", "[emoji=\\ud83d\\udc36]" -1)
	tempMsg = strings.Replace(tempMsg, "üé∞", "[emoji=\\ud83c\\udfb0]" -1)
	return tempMsg
}